using Microsoft.Toolkit.Mvvm.Messaging.Messages;
using Windows.Foundation;
using static Enums;

namespace Scanner.Services.Messenger
{
    class PreviewSelectedRegionChangedMessage : ValueChangedMessage<Rect?>
    {
        /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
        // DECLARATIONS /////////////////////////////////////////////////////////////////////////////////////////////////////////
        /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
        public Rect? SelectedRegionInches;


        /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
        // CONSTRUCTORS / FACTORIES /////////////////////////////////////////////////////////////////////////////////////////////
        /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
        public PreviewSelectedRegionChangedMessage(Rect? selectedRegionInches) : base(selectedRegionInches)
        {
            SelectedRegionInches = selectedRegionInches;
        }
    }
}
