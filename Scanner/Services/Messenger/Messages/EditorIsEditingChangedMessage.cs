﻿using Microsoft.Toolkit.Mvvm.Messaging.Messages;

namespace Scanner.Services.Messenger
{
    class EditorIsEditingChangedMessage : ValueChangedMessage<bool>
    {
        /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
        // DECLARATIONS /////////////////////////////////////////////////////////////////////////////////////////////////////////
        /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
        bool IsEditing;

        /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
        // CONSTRUCTORS / FACTORIES /////////////////////////////////////////////////////////////////////////////////////////////
        /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
        public EditorIsEditingChangedMessage(bool isEditing) : base(isEditing)
        {
            IsEditing = isEditing;
        }
    }
}