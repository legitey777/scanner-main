﻿using Microsoft.Toolkit.Mvvm.Messaging.Messages;

namespace Scanner.Services.Messenger
{
    class PageListCurrentIndexChangedMessage : ValueChangedMessage<int>
    {
        /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
        // DECLARATIONS /////////////////////////////////////////////////////////////////////////////////////////////////////////
        /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
        int NewIndex;

        /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
        // CONSTRUCTORS / FACTORIES /////////////////////////////////////////////////////////////////////////////////////////////
        /////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
        public PageListCurrentIndexChangedMessage(int index) : base(index)
        {
            NewIndex = index;
        }
    }
}